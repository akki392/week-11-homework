package Question3;

import java.util.ArrayList;

public class ArrayColour {

    public static void main(String[] args) {
        ArrayList<String> colours = new ArrayList<>();
        colours.add("blue");
        colours.add("black");
        colours.add("red");
        colours.add("yellow");
        colours.add("pink");
        colours.add("purple");
        colours.add("orange");
        colours.add("green");
        colours.add("white");
        for (String a:colours)
        {
            System.out.println(a);
        }
    }
}
